export type Language = keyof typeof translations
export type TranslationKey = string

export function useTranslation(lang: Language) {
  return {
    t: (key: string) => {
      const keys = key.split('.')
      let value: any = translations[lang]

      for (const k of keys) {
        value = value?.[k]
      }

      return value || key
    },
  }
}
